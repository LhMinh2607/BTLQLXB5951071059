--CREATE DATABASE BusManager
use BusManager

CREATE TABLE HTX
(
	MaHTX varchar(10) primary key not null,
	TenHTX nvarchar(100)  not null,
	DCHTX  nvarchar(50) not null
)

CREATE TABLE BUS
(
	MaTuyen varchar(10) primary key not null,
	MaHTX varchar(10) constraint FK_BUS_MHTX foreign key references HTX(MaHTX) not null,
	TenTuyen nvarchar(100) not null,
	ThoiGian_MotChuyen int not null,
	SoChoNgoi int not null,
	SoChuyen int not null,
	KhoiHanh  time not null,
	KetThuc	 time not null,
	constraint check_BUS check (KhoiHanh != KetThuc) 
)	

CREATE TABLE STAFF
(
	MaNV varchar(20) primary key not null,
	MaTuyen varchar(10) constraint FK_ST_MT foreign key references BUS(MaTuyen) not null,
	HoTen nvarchar(50) not null,
	NgaySinh smalldatetime not null, 
	Luong money not null,
	DiaChi nvarchar(100) not null,
	GioiTinh nvarchar(5) not null,
	Chucvu nvarchar(50) not null,
	--Doanh_Thu_Trong_Ngay int not null,
	So_Ve_Ban_Ra_Trong_Ngay int not null,
	TenTuyen nvarchar(50) not null
)


CREATE TABLE TICKET_PACK
(
	MaTapVe varchar(10) primary key not null,
	MaNV varchar(20) constraint FK_TK_MNV foreign key references STAFF(MaNV)not null,
	DonGiaVe int not null,   
	LoaiVe nvarchar(50) not null
)



CREATE TABLE STATION
(
	MaTramDung varchar(50) primary key  not null,
	TenTramDung nvarchar(50) not null,
)	





CREATE TABLE CROSS_STATION
(
	MaTuyen varchar(10) constraint FK_CST_MT foreign key references BUS(MaTuyen) not null,
	MaTramDung varchar(50) constraint FK_CST_MTD  foreign key references STATION(MaTramDung) not null,
	constraint PK_ST_MTD_MT primary key (MaTuyen, MaTramDung),
	GianCachChuyen int not null,
	STT_TramDung int not null,
)	


		

